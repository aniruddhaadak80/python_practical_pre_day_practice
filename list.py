list1 = [1,2,3];
list2 = [4,5,6];

# Add two lists using + operator
list1.extend(list2);

# list into a list 
list1.append(list2);

print(list1);

# reverse the list called list1 
list1.reverse();
print(list1);

# pop the first element from list ...
list1.pop(0);
print(list1);

# remove element from list using remove function ....
list1.remove(3);
print(list1);

