import turtle;
turtle.title("Red circle with radius 100px"); #title -> red circle with radius 100px
turtle.bgcolor("light blue");
turtle.shape("turtle"); #shape -> turtle 
turtle.fillcolor("red"); #fill the circle with red color 
turtle.begin_fill();
turtle.circle(100); # radius of circle -> 100px
turtle.hideturtle();
turtle.end_fill();
turtle.done(); 